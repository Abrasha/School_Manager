package school_manager.model.overviews;

/**
 *
 * @author abrasha
 */
public class SubjectOverview {

    private int id;
    private String name;

    public SubjectOverview(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

}
